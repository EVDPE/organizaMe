package Logica;

public class Filme {

    Long id;
    String titulo;
    String pais;
    String diretor;
    String trilha_sorora;
    String tempo_duracao;
    String ano_lancamento;
    String idioma;
    String trailer;
    String genero;
    String ja_assistiu;
    String sinopse;
    String comentario;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public String getPais() {
        return pais;
    }

    public void setPais(String pais) {
        this.pais = pais;
    }

    public String getDiretor() {
        return diretor;
    }

    public void setDiretor(String diretor) {
        this.diretor = diretor;
    }

    public String getTrilha_sonora() {
        return trilha_sorora;
    }

    public void setTrilha_sonora(String trilha_sorora) {
        this.trilha_sorora = trilha_sorora;
    }

    public String getTempo_duracao() {
        return tempo_duracao;
    }

    public void setTempo_duracao(String tempo_duracao) {
        this.tempo_duracao = tempo_duracao;
    }

    public String getAno_lancamento() {
        return ano_lancamento;
    }

    public void setAno_lancamento(String ano_lancamento) {
        this.ano_lancamento = ano_lancamento;
    }

    public String getIdioma() {
        return idioma;
    }

    public void setIdioma(String idioma) {
        this.idioma = idioma;
    }

    public String getTrailer() {
        return trailer;
    }

    public void setTrailer(String trailer) {
        this.trailer = trailer;
    }

    public String getGenero() {
        return genero;
    }

    public void setGenero(String genero) {
        this.genero = genero;
    }

    public String getJa_assistiu() {
        return ja_assistiu;
    }

    public void setJa_assistiu(String ja_assistiu) {
        this.ja_assistiu = ja_assistiu;
    }

    public String getSinopse() {
        return sinopse;
    }

    public void setSinopse(String sinopse) {
        this.sinopse = sinopse;
    }
    
    public String getComentario(){
        return comentario;
    }
    
    public void setComentario(String comentario){
        this.comentario = comentario;
    }

}
